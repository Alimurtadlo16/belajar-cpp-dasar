#include <iostream>
using namespace std;
int main() {
    int i=1;
    int total;
    for (i=1; total; i++) {
        cout << "Nilai i: ";
        cin >> total;
    } while (i += total) {
        cout << "Total sekarang: " << total << endl;
    }
    cout << "Total akhir: " << total << endl;
    return 0;
}